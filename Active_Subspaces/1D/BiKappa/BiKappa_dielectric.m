function omega = BiKappa_dielectric(k,sigma1,sigma2,mu1,mu2,beta,kappa,init_guess)

    if(kappa == 2)
        D = @(omega) 1+(-1).*k.^(-2).*(2.*beta.*k.^2.*(2.*((-1).*k.*mu1+omega).^2+k.^2.* ...
          sigma1.^2).^(-3).*(4.*((-1).*k.*mu1+omega).^4+12.*k.^2.*((-1).*k.*mu1+ ...
          omega).^2.*sigma1.^2+(sqrt(-1)*8).*2.^(1/2).*k.^3.*(k.*mu1+(-1).*omega) ...
          .*sigma1.^3+(-3).*k.^4.*sigma1.^4)+2.*((-1)+beta).*k.^2.*(2.*((-1).*k.* ...
          mu2+omega).^2+k.^2.*sigma2.^2).^(-3).*((-4).*((-1).*k.*mu2+omega).^4+( ...
          -12).*k.^2.*((-1).*k.*mu2+omega).^2.*sigma2.^2+(sqrt(-1)*(-8)).*2.^(1/2) ...
          .*k.^3.*(k.*mu2+(-1).*omega).*sigma2.^3+3.*k.^4.*sigma2.^4));
    elseif(kappa == 6)
        D = @(omega) 1+(-1).*k.^(-2).*((2/7).*beta.*k.^2.*(2.*((-1).*k.*mu1+omega).^2+9.* ...
          k.^2.*sigma1.^2).^(-7).*(448.*((-1).*k.*mu1+omega).^12+14784.*k.^2.*(( ...
          -1).*k.*mu1+omega).^10.*sigma1.^2+213840.*k.^4.*((-1).*k.*mu1+omega) ...
          .^8.*sigma1.^4+1796256.*k.^6.*((-1).*k.*mu1+omega).^6.*sigma1.^6+ ...
          10103940.*k.^8.*((-1).*k.*mu1+omega).^4.*sigma1.^8+54561276.*k.^10.*(( ...
          -1).*k.*mu1+omega).^2.*sigma1.^10+(sqrt(-1)*60466176).*2.^(1/2).*k.^11.* ...
          (k.*mu1+(-1).*omega).*sigma1.^11+(-40920957).*k.^12.*sigma1.^12)+(2/7).* ...
          ((-1)+beta).*k.^2.*(2.*((-1).*k.*mu2+omega).^2+9.*k.^2.*sigma2.^2).^(-7) ...
          .*((-448).*((-1).*k.*mu2+omega).^12+(-14784).*k.^2.*((-1).*k.*mu2+omega) ...
          .^10.*sigma2.^2+(-213840).*k.^4.*((-1).*k.*mu2+omega).^8.*sigma2.^4+( ...
          -1796256).*k.^6.*((-1).*k.*mu2+omega).^6.*sigma2.^6+(-10103940).*k.^8.*( ...
          (-1).*k.*mu2+omega).^4.*sigma2.^8+(-54561276).*k.^10.*((-1).*k.*mu2+ ...
          omega).^2.*sigma2.^10+(sqrt(-1)*(-60466176)).*2.^(1/2).*k.^11.*(k.*mu2+( ...
          -1).*omega).*sigma2.^11+40920957.*k.^12.*sigma2.^12));
    else
        disp('Error: kappa is not equal to 2 or 6');
        pause;
    end

    options = optimoptions('fsolve','Display','off');
    omega = fsolve(D, init_guess, options);
end